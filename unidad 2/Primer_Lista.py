#[] Es para listas
Lista=["Juan", "Ivan", "Adrian", "Jair"]
print (Lista)
#seleccionar un elemento de la lista
print (Lista[2])
#append es solo para agregar un solo elemento
Lista.append("alexa")
print (Lista)
Lista.insert(1,"Javier")
print (Lista)
#pop es para eliminar un elemento de la lista
Lista.pop()
print (Lista)
Lista.pop(1)
print (Lista)